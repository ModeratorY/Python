# ЦИКЛ WHILE В PYTHON

"""
    Цикл while используется, когда невозможно определить точное количество повторений цикла.
"""

cnt = 0 
print() # для красивого отступа в выводе в терминале
while cnt < 5: # ключевое слово 'while' < условие выполнение цикла > :
    print("Значение cnt =", cnt) 
    cnt += 1 

print("А всё, цикл закончился")


print("\n==========================================\n")


''' Цикл while может быть бесконечным '''

i = 0
while True: # условие всегда истинно
    print("Значение i =", i)
    i += 1

    if i > 100: # чтобы терминал с ума не сходил сделаем условие для завершения
        break   # break - прерывает выполнение цикла

print("Суть бесконечного цикла понятна :)")


print("\n==========================================\n")


''' Оператор continue начинает повторение цикла заново '''

i = 0 
while i < 5: 

    i += 1

    if i % 2 == 1:
        continue    # оператор continue начинает повторение цикла заново
   
    # в случае вызова continue код ниже не выполнится
    print("Значение i =", i )


print("\n==========================================\n")


''' Как и для цикла for, для цикла while мы можем записать конструкцию else '''

x = 1
while x < 5:
    print("Значение x =", x)
    x += 1

else:
    print('Цикл завершен')


print("\n==========================================\n")


