''' Пользователь вводит числа A и B (A > B). Выведите все числа от A до B включительно '''

A = int(input('Введите число A: '))
B = int(input('Введите число B: '))

while A >= B:
    print(A)
    A -= 1



''' 
    Пользователь вводит числа до тех пор, пока не введет 0.
    Выведите количество введенных чисел (0 считать не нужно).
'''

n = int(input('\nВведите число: '))
counter = 0    
while bool(n): 
    n = int(input('Введите число: ')) 
    counter += 1 

print('Количество чисел', counter, end = '\n\n')



'''
    Пользователь вводит число N (N > 1)
    Выведите его наименьший делитель (не считая 1)
'''

N = int(input('Введите число N: '))
div = 2

while N % div != 0:
    div += 1

print('Наименьший делитель равен =', div, end = '\n\n')