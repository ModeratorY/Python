print("\nЗадача 6: Пользователь вводит число N. Выведите факториал число N\n")

N = int(input("Введите натуральное число N: "))
Factorial = 1

if N < 0: 
    print("Просил же, натуральное...")

elif N == 0: 
        Factorial = 1
        print("Факториал числа N:", Factorial)

else:
    for a in range(1, N+1):
        Factorial *= a

    print("Факториал числа N:", Factorial)



