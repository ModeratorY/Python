print("\nЗадача 3: Пользователь вводит числа до тех пор, пока не введет 0. Выведите сумму введенных чисел (0 считать не нужно).\n")

Sum = 0
while True:
    num = int(input("Введите число (0 - выход): "))

    if num == 0:
        break

    Sum += num

print("Сумма введённых чисел =", Sum)



print("\nЗадача 4: Пользователь вводит числа до тех пор, пока не введет 0. Выведите максимальное введенное число (0 считать не нужно).\n")

Max = -99999999
