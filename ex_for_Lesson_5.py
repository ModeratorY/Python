F1 = F2 = 1

if N < 0:
    print("Надо было ввести натуральное число!\n")

elif N == 0:
    print("Нулевого члена в последовательности нет\n")

elif N == 1 or N == 2:
    print("N-ное число Фибоначчи:", F1, end = '\n\n')

else:
    i = 0          
    while i < N - 2:

        FN = F1 + F2
        F1 = F2
        F2 = FN

        i += 1

    print("N-ное число Фибоначчи:", FN, end = '\n\n')




